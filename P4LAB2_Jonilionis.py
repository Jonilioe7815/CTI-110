#import the drawing brush
import turtle
g = turtle.Turtle()

#change the brush
g.pensize("20")         
g.pencolor("red")     
g.shape("circle")

#draw the E    

g.forward(80)
g.backward(80)
g.right(90)
g.forward(60)
g.left(90)
g.forward(70)
g.backward(70)
g.right(90)
g.forward(60)
g.left(90)
g.forward(80)
g.penup()
g.forward(60)
g.left(90)
g.forward(120)
g.right(90)
g.pendown()

